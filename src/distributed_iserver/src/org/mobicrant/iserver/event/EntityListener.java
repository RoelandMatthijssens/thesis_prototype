package org.mobicrant.iserver.event;

/**
 * Listener interface to handle entity-specific events.
 * @version 1.0 Feb 20, 2006
 * @author Beat Signer, signer@inf.ethz.ch
 */
public interface EntityListener {

   void propertyAdded(PropertyAddRemoveEvent e);


   void propertyRemoved(PropertyAddRemoveEvent e);


   void authorisedUserAdded(UserAddRemoveEvent e);


   void authorisedUserRemoved(UserAddRemoveEvent e);


   void unauthorisedUserAdded(UserAddRemoveEvent e);


   void unauthorisedUserRemoved(UserAddRemoveEvent e);

}